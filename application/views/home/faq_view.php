<h2><i>Frequently Asked Questions</i></h2>


<div id="faq">

	<h3>Will I get in trouble for anything I do with the music I download?</h3>
	<div>
		<p>All of the music on ReZound is free of DRM or any spyware.  When you make a purchase, you thereafter own the item you purchased, and what you do with it is your own business.  Anything less would be absurd.</p>
	</div>

	<h3>I don't believe what you said above about owning what I purchase.  Won't the artist try to sue me?</h3>
	<div>
		<p>No.  Artists on ReZound are not signed to a record label, and they must agree to distribute their music free of any DRM and waive the right to pursue litigation.</p>
	</div>
	
	<h3>If an artist on ReZound signs with a record label, what happens to the music I already bought from that artist?</h3>
	<div>
		<p>We will make every effort to protect your ownership rights.  We expect these cases to be rare; our hope is that a successful artist will have no motivation to move to a record label.</p>
	</div>
	
	<h3>Okay.  Then what's stopping me from copying and distributing an artist's music without permission?</h3>
	<div>
		<p>You are.  It's simple, really: free, unregulated circulation of an artist's music deprives the artist of sales.  An artist with no sales will not have the means to keep making the music you like.  End of story.</p>
		<p>One more thing: at ReZound, we recognize that sometimes giving away an artist's music to a friend can actually benefit the artist.  Much like in the days of the mix tape, exposing a potential fan can lead to more support for the artist.  We encourage this, insofar as your action does not challenge the artist's ability to keep producing their creative works.</p>
	</div>
	
	<h3>Will the music I hear on ReZound contain cursing or suggestive themes?</h3>
	<div>
		<p>At ReZound, we believe any artist should have the freedom to express their creativity in any way they deem fit.  Therefore we neither support nor participate in censorship.  Users who find language or other types of content offensive should browse at their own risk.</p>
	</div>
	  
	<h3>How are the ReZound staff supported?</h3>
	<div>
		<p>ReZound proceeds are linked directly to those of the artists.  We take a percentage of any sales (varying depending on the type of sale).  This means if the artists don't succeed, neither do we.</p>
	</div>

	<h3>I don't see any physical music media (CDs, vinyl, etc) for sale on ReZound.  What if I want to buy physical media?  And what about T-shirts?</h3>
	<div>
		<p>At the moment, we have no means of selling physical media or facilitating its sale by the artists.  We recommend that you contact the artist directly.  The same goes for other merch such as clothing or patches.</p>
	</div>

	<h3>If I attend a live show by an artist on ReZound, how can I link any music purchases to my ReZound library?</h3>
	<div>
		<p>You can't - <i>yet</i>.  Stay tuned, and make sure you have a smartphone.</p>
	</div>
	
	<h3>Your smartphone app is only available on Android, but I have an iPhone.  Do you hate Apple users?</h3>
	<div>
		<p>Not all of them, and not because they use Apple products.  We simply haven't gotten around to writing an app for iOS yet; please bear with us.</p>
	</div>
	



